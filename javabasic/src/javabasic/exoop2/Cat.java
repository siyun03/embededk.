package javabasic.exoop2;

public class Cat {
	
	int life; // 수명
	String model; // 종 이름
	int type; // 종류의 수
	int price; // 분양가격
	public Cat(int life, String model, int type, int price) {
		super();
		this.life = life;
		this.model = model;
		this.type = type;
		this.price = price;
	}

}
